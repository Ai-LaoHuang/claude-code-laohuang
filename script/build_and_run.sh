#!/usr/bin/env bash

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
APP_NAME="Claude Code LaoHuang"
APP_BUNDLE="${ROOT_DIR}/desktop/build-artifacts/macos-arm64/${APP_NAME}.app"
BUILD_SCRIPT="${ROOT_DIR}/desktop/scripts/build-macos-arm64.sh"
BRIDGE_ROOT="${OPENCLAW_CODEX_BRIDGE_ROOT:-/Users/xiaowo1800gmail.com/Documents/cli/cloudecode/bridge-auto}"
BRIDGE_HOST="${OPENCLAW_CODEX_BRIDGE_HOST:-127.0.0.1}"
BRIDGE_PORT="${OPENCLAW_CODEX_BRIDGE_PORT:-8644}"
BRIDGE_URL="http://${BRIDGE_HOST}:${BRIDGE_PORT}"
BRIDGE_TOKEN="${CLAUDE_CODE_BRIDGE_TOKEN:-openclaw-codex-bridge}"
MODEL="${ANTHROPIC_MODEL:-gpt-5.5}"
CONFIG_DIR="${CLAUDE_CONFIG_DIR:-${ROOT_DIR}/.desktop-gpt55-claude}"
USE_CODEX_BRIDGE="${USE_CODEX_BRIDGE:-0}"
PROCESS_NAMES=(
  "claude-code-desktop"
  "${APP_NAME}"
)

usage() {
  cat <<'EOF'
Build and run the Claude Code LaoHuang macOS desktop app.

Usage:
  ./script/build_and_run.sh [--rebuild] [--verify]

Options:
  --rebuild   Force a fresh macOS app bundle build before launch.
  --verify    Confirm the launched process exists after opening the app.

Environment:
  USE_CODEX_BRIDGE=1  Start and configure the local Codex GPT bridge.
EOF
}

append_no_proxy() {
  local current="${1:-}"
  local loopback="127.0.0.1,localhost,::1"
  if [[ -n "$current" ]]; then
    printf '%s,%s' "$current" "$loopback"
  else
    printf '%s' "$loopback"
  fi
}

REBUILD=0
VERIFY=0

while [[ $# -gt 0 ]]; do
  case "$1" in
    --rebuild)
      REBUILD=1
      shift
      ;;
    --verify)
      VERIFY=1
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "[build_and_run] Unknown argument: $1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

stop_running_app() {
  echo "[build_and_run] Stopping existing Claude Code LaoHuang desktop processes..."
  for name in "${PROCESS_NAMES[@]}"; do
    pkill -x "${name}" 2>/dev/null || true
  done

  # Stop sidecars launched from this workspace without touching unrelated
  # Claude Code or VS Code Claude processes.
  pkill -f "${ROOT_DIR}/desktop/src-tauri/target/.*/claude-sidecar server" 2>/dev/null || true
  pkill -f "${ROOT_DIR}/desktop/build-artifacts/macos-arm64/${APP_NAME}.app/Contents/MacOS/claude-sidecar server" 2>/dev/null || true
}

build_app_if_needed() {
  if [[ "${REBUILD}" == "1" || ! -d "${APP_BUNDLE}" ]]; then
    echo "[build_and_run] Building fresh macOS app bundle..."
    (
      cd "${ROOT_DIR}"
      SKIP_INSTALL=1 PRESERVE_TAURI_TARGET=1 "${BUILD_SCRIPT}"
    )
  fi
}

ensure_bridge() {
  if [[ "${USE_CODEX_BRIDGE}" != "1" ]]; then
    echo "[build_and_run] USE_CODEX_BRIDGE is not set; preserving existing provider config."
    return 0
  fi

  export NO_PROXY="$(append_no_proxy "${NO_PROXY:-}")"
  export no_proxy="$(append_no_proxy "${no_proxy:-}")"

  if python3 - <<PY >/dev/null 2>&1
import urllib.request
urllib.request.urlopen("${BRIDGE_URL}/healthz", timeout=2)
PY
  then
    return 0
  fi

  if [[ ! -x "${BRIDGE_ROOT}/openclaw-codex-bridge.sh" ]]; then
    echo "[build_and_run] Bridge is not running and launcher was not found: ${BRIDGE_ROOT}/openclaw-codex-bridge.sh" >&2
    exit 1
  fi

  echo "[build_and_run] Starting Codex GPT-5.5 bridge at ${BRIDGE_URL}..."
  nohup "${BRIDGE_ROOT}/openclaw-codex-bridge.sh" >/dev/null 2>&1 &
  sleep 1

  python3 - <<PY >/dev/null 2>&1
import urllib.request
urllib.request.urlopen("${BRIDGE_URL}/healthz", timeout=5)
PY
}

write_provider_config() {
  if [[ "${USE_CODEX_BRIDGE}" != "1" ]]; then
    return 0
  fi

  mkdir -p "${CONFIG_DIR}/cc-haha"
  cat > "${CONFIG_DIR}/settings.json" <<JSON
{
  "defaultMode": "bypassPermissions"
}
JSON

  cat > "${CONFIG_DIR}/cc-haha/providers.json" <<JSON
{
  "schemaVersion": 1,
  "activeId": "codex-gpt55-bridge",
  "providers": [
    {
      "id": "codex-gpt55-bridge",
      "presetId": "custom",
      "name": "Codex GPT-5.5 Bridge",
      "apiKey": "${BRIDGE_TOKEN}",
      "authStrategy": "auth_token",
      "baseUrl": "${BRIDGE_URL}",
      "apiFormat": "anthropic",
      "models": {
        "main": "${MODEL}",
        "haiku": "gpt-5.4-mini",
        "sonnet": "${MODEL}",
        "opus": "${MODEL}"
      },
      "notes": "Local Codex GPT-5.5 bridge profile generated by script/build_and_run.sh."
    }
  ]
}
JSON
}

launch_app() {
  if [[ ! -d "${APP_BUNDLE}" ]]; then
    echo "[build_and_run] App bundle not found: ${APP_BUNDLE}" >&2
    exit 1
  fi

  echo "[build_and_run] Refreshing LaunchServices metadata for the app bundle..."
  /System/Library/Frameworks/CoreServices.framework/Frameworks/LaunchServices.framework/Support/lsregister \
    -f "${APP_BUNDLE}" >/dev/null 2>&1 || true

  touch "${APP_BUNDLE}"
  echo "[build_and_run] Launching ${APP_BUNDLE}"
  local open_args=(
    -n
    --env "CLAUDE_CONFIG_DIR=${CONFIG_DIR}"
    --env "CC_HAHA_DESKTOP_STRICT_MCP_CONFIG=1"
    --env "NO_PROXY=${NO_PROXY:-}"
    --env "no_proxy=${no_proxy:-}"
  )

  if [[ "${USE_CODEX_BRIDGE}" == "1" ]]; then
    open_args+=(
      --env "ANTHROPIC_API_KEY=${BRIDGE_TOKEN}"
      --env "ANTHROPIC_AUTH_TOKEN=${BRIDGE_TOKEN}"
      --env "ANTHROPIC_BASE_URL=${BRIDGE_URL}"
      --env "ANTHROPIC_MODEL=${MODEL}"
      --env "ANTHROPIC_DEFAULT_HAIKU_MODEL=gpt-5.4-mini"
      --env "ANTHROPIC_DEFAULT_SONNET_MODEL=${MODEL}"
      --env "ANTHROPIC_DEFAULT_OPUS_MODEL=${MODEL}"
    )
  fi

  /usr/bin/open "${open_args[@]}" "${APP_BUNDLE}"
}

verify_launch() {
  if [[ "${VERIFY}" != "1" ]]; then
    return 0
  fi

  sleep 3
  if pgrep -x "claude-code-desktop" >/dev/null || pgrep -x "${APP_NAME}" >/dev/null; then
    echo "[build_and_run] Verified: ${APP_NAME} is running."
  else
    echo "[build_and_run] ERROR: ${APP_NAME} did not appear to launch." >&2
    exit 1
  fi
}

stop_running_app
build_app_if_needed
ensure_bridge
write_provider_config
launch_app
verify_launch
